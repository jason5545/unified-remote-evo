                            Text(
                                text = "ÈüåÈ´îÁâàÊú¨Ôºö$fwVersion",
                                style = MaterialTheme.typography.bodyMedium
                            )
                        }

                        // ËªüÈ´îÁâàÊú¨ÔºàÁî®ÊñºÂà§Êñ∑ÊòéÊñáÂØÜÁ¢ºÔºâ

                            Text(
                                text = "Ë£ùÁΩÆÊ®°ÂºèÔºö${deviceMode.name}",
                                style = MaterialTheme.typography.bodyMedium,
                                color = when(deviceMode) {
                                    com.unifiedremote.evo.network.ble.DeviceMode.COMPOSITE -> Color(0xFF4CAF50)  // Á∂†Ëâ≤

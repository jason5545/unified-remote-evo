                        DropdownMenu(
                            expanded = showLogLevelMenu,
                            onDismissRequest = { showLogLevelMenu = false }
                        ) {
                            ConnectionLogger.LogLevel.values().forEach { level ->
                                DropdownMenuItem(
                                    text = {
                                        Row(
                                            horizontalArrangement = Arrangement.spacedBy(8.dp)
                                        ) {
                                            Text(
                                                text = when (level) {
                                                    ConnectionLogger.LogLevel.DEBUG -> "üîç"
                                                    ConnectionLogger.LogLevel.INFO -> "‚ÑπÔ∏è"
                                                    ConnectionLogger.LogLevel.WARNING -> "‚ö†Ô∏è"

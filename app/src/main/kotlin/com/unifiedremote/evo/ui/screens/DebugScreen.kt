                            Spacer(modifier = Modifier.height(2.dp))
                            Text(
                                text = "ËªüÈ´îÁâàÊú¨Ôºö$swVersion",
                                style = MaterialTheme.typography.bodyMedium
                            )
                        }

                        Spacer(modifier = Modifier.height(2.dp))
                        Text(
                            text = "ÂàáÊèõÊåá‰ª§Ôºö${vendorInfo.switchCommandType}",
                            style = MaterialTheme.typography.bodyMedium
                        )

                        // Ë£ùÁΩÆÊ®°ÂºèÔºàÂÉÖÂú®Â∑≤ÈÄ£Á∑öÊôÇÈ°ØÁ§∫Ôºâ
